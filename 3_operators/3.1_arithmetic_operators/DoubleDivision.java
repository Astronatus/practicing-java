class DoubleDivision {
    public static void main(String[] args) {
        int numOrders = 100;
        int days = 30;

        double avgOrders = numOrders / days;


        System.out.println(avgOrders); // value is 3.0, double

        // Double in the division
        System.out.println(numOrders/30.0); // Value is 3.333333, double

    }
}